#include "libft.h"

char	*ft_strnstr(char *big, char *little, int len)
{
	int	i;
	int	j;

	i = 0;
	if (!big && len == 0)
		return (NULL);
	if (!little[0])
		return ((char *)big);
	while (big[i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && i + j < len)
		{
			if (!little[j + 1])
				return ((char *)big + i);
			j++;
		}
		i++;
	}
	return (NULL);
}
