#include "libft.h"

int	ft_strlcpy(char *dst, char *src, int size)
{
	int	len;
	int	i;

	i = 0;
	len = 0;
	while (src[len])
		len++;
	if (size == 0)
		return (len);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (len);
}
